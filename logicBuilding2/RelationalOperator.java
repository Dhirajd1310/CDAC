class RelationalOperator{
	public static void main(String args[]){
		int vari1 = 50;
		int vari2 = 60;
		System.out.println(vari1==vari2);
		System.out.println(vari1!=vari2);
		System.out.println(vari1<vari2);
		System.out.println(vari1<=vari2);
		System.out.println(vari1>vari2);
		System.out.println(vari1>=vari2);
	}
}